//60) Faça um programa que exiba os primeiros 20 números da sequência de Fibonacci usando o loop "do-while".
package exerciciosjava;
import java.util.*;

public class Ex060 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int c = 0;
        do {
            c++;
        } while (c <= 10);
    }
}
